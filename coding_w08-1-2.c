#include <stdio.h>

int main(){
    int i ;

    // แสดงเลขคู่ด้วย for loop
    for (i = 2; i <= 10 ; i++)
    {
        printf("%d ", i++);
    }
    printf("\nEnd of loop \n");
    return 0;
}

//กำหนด i ให้เริ่มที่ 2 และถ้า i น้อยกว่าหรือเท่ากับ 10 ใช้ i ก่อนและจะบวก 1 ทุกครั้งของเงื่อนไข
//ปริ้น i ออกมาและเพิ่ม i ไปอีก 1 ทำซ้ำจน i ไม่น้อยกว่าหรือ 10
//จบการทำซ้ำปริ้น End of loop